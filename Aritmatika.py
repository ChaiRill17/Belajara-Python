print("--- Operator Aritmatika ---")

print(10 + 5)   # Penjumlahan → 15
print(10 - 5)   # Pengurangan → 5
print(10 * 5)   # Perkalian → 50
print(10 / 5)   # Pembagian → 2.0
print(10 % 3)   # Sisa bagi → 1, 10 dibagi 3 = 3 sisa 1 → jadi hasilnya 1
print(10 // 3)  # Pembagian angka bulat → 3 
print(2 ** 3)   # Pangkat → 8
