# Operator Perbandingan (True / False)

print("--- OPERATOR PERBANDINGAN (TRUE / FALSE) ---\n")

print("10 > 5  =", 10 > 5)     # lebih dari
print("10 < 5  =", 10 < 5)     # kurang dari
print("10 >= 10 =", 10 >= 10)  # lebih dari atau sama dengan
print("3 <= 5  =", 3 <= 5)     # kurang dari atau sama dengan
print("10 == 10 =", 10 == 10)  # sama dengan
print("10 != 2  =", 10 != 2)   # tidak sama dengan